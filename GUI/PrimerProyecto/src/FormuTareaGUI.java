    //ENVIAR FORMULARIO
    private void enviarFormulario() {

        String nombre = jTextField1.getText();
        String telefono = jTextField2.getText();
        String genero = jRadioButton1.isSelected() ? "Masculino" :
                jRadioButton2.isSelected() ? "Femenino" : "No seleccionado";

        String dia = (String) jComboBox1.getSelectedItem();
        String mes = (String) jComboBox2.getSelectedItem();
        String anio = (String) jComboBox3.getSelectedItem();

        String direccion = jTextArea1.getText();
        String urlImagen = jTextField3.getText();
        boolean acepta = jCheckBox1.isSelected();

        if (!acepta) {
            jLabel8.setText("Debes aceptar los términos y condiciones.");
            return;
        }

        String resultado = "Nombre: " + nombre +
                "\nTeléfono: " + telefono +
                "\nGénero: " + genero +
                "\nFecha Nacimiento: " + dia + "/" + mes + "/" + anio +
                "\nDirección: " + direccion;
                

        jTextArea2.setText(resultado);

        // Cargar imagen
         try {
            java.net.URL url = new java.net.URL(urlImagen);
            ImageIcon icon = new ImageIcon(url);
            Image img = icon.getImage(); 
            Image newImg = img.getScaledInstance(170, 150, Image.SCALE_SMOOTH); 
            ImageIcon scaledIcon = new ImageIcon(newImg);
            jLabel7.setIcon(scaledIcon);
            jLabel7.setText("");
        } catch (Exception ex) {
            jLabel8.setText("URL es invalida");
        }
    }

